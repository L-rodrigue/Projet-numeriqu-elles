      <li>diaporama</li>
      <li>questionnaire en ligne</li>
      <li>outils disponibles sur l’ENT : Madmagz, mur collaboratif, blog, cahier multimédia, pad</li>
    </ul>
    <p>Les productions seront à déposer pour le 17 janvier 2020 dans le groupe Numériqu’elles
    créé sur l’ENT.</p>
    <p>Un jury se réunira courant février : les réalisations les plus réussies seront récompensées et serviront à la promotion du forum l’année prochaine.</p>
    <p>Les établissements qui le souhaitent peuvent être accompagnés dans ce projet par la Délégation académique au numérique éducatif : ce.dane@ac-amiens.fr</p>
    <p>Chaque production, réalisée en groupe qui pourra être mixte, devra être accompagnée des éléments suivants (qui seront sur l’ENT) :</p>
