    <p>Suite au forum Numériqu’elles du 8 novembre 2019, il est proposé aux élèves des établissements ayant participé de réaliser une ou plusieurs productions utilisant le numérique pour promouvoir ce secteur auprès des collégiennes et des lycéennes notamment.<br>
